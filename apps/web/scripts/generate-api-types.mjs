import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '../../..');
const inputPath = resolve(root, 'schemas/api/trace-weft.openapi.json');
const outputPath = resolve(import.meta.dirname, '../src/generated/api-types.ts');
const openapi = JSON.parse(readFileSync(inputPath, 'utf8'));
const schemas = openapi.components?.schemas ?? {};

const pascal = (name) => name.replace(/(^|[_-])([a-z])/g, (_, _sep, c) => c.toUpperCase());
const propertyName = (key) => (/^[A-Za-z_$][\w$]*$/.test(key) ? key : JSON.stringify(key));

function typeFor(schema) {
  if (!schema || Object.keys(schema).length === 0) return 'unknown';
  if (schema.$ref) return pascal(schema.$ref.split('/').pop());
  if (schema.anyOf) return schema.anyOf.map(typeFor).join(' | ');
  if (Array.isArray(schema.type)) {
    return schema.type.map((t) => typeFor({ ...schema, type: t })).join(' | ');
  }
  switch (schema.type) {
    case 'string':
      return 'string';
    case 'number':
    case 'integer':
      return 'number';
    case 'boolean':
      return 'boolean';
    case 'null':
      return 'null';
    case 'array':
      return `${typeFor(schema.items)}[]`;
    case 'object':
      if (schema.properties) return inlineObject(schema);
      if (schema.additionalProperties && typeof schema.additionalProperties === 'object') {
        return `Record<string, ${typeFor(schema.additionalProperties)}>`;
      }
      return 'Record<string, unknown>';
    default:
      return 'unknown';
  }
}

function inlineObject(schema) {
  const required = new Set(schema.required ?? []);
  const props = Object.entries(schema.properties ?? {})
    .map(
      ([key, value]) => `  ${propertyName(key)}${required.has(key) ? '' : '?'}: ${typeFor(value)};`,
    )
    .join('\n');
  const index = schema.additionalProperties ? '\n  [key: string]: unknown;' : '';
  return `{\n${props}${index}\n}`;
}

const lines = [
  '// Generated by scripts/generate-api-types.mjs from schemas/api/trace-weft.openapi.json.',
  '// Do not edit by hand.',
  '',
];

for (const [name, schema] of Object.entries(schemas)) {
  lines.push(`export type ${pascal(name)} = ${typeFor(schema)};`, '');
}

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, `${lines.join('\n').trimEnd()}\n`);
